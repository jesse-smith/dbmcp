from tests.performance.benchmark import Benchmark, BenchmarkResult


@dataclass
class NFRResult:
    """Result of NFR validation."""

    nfr_id: str
    description: str
    threshold: str
    actual_value: str
    passed: bool
    notes: str = ""


@dataclass
class PerformanceReport:
    """Complete performance report."""

    timestamp: datetime
    python_version: str
    benchmarks: list[dict[str, Any]] = field(default_factory=list)
    nfr_results: list[NFRResult] = field(default_factory=list)
    scaling_data: dict[str, list[dict[str, Any]]] = field(default_factory=dict)
    notes: list[str] = field(default_factory=list)

    def to_markdown(self) -> str:
        """Generate markdown report."""
        lines = [
            "# Performance Baseline Report",
            "",
            f"**Generated**: {self.timestamp.isoformat()}",
            f"**Python Version**: {self.python_version}",
            "",
            "---",
            "",
            "## NFR Compliance Summary",
            "",
            "| NFR | Description | Threshold | Actual | Status |",
            "|-----|-------------|-----------|--------|--------|",
        ]

        for nfr in self.nfr_results:
            status = "PASS" if nfr.passed else "**FAIL**"
            lines.append(
                f"| {nfr.nfr_id} | {nfr.description} | {nfr.threshold} | {nfr.actual_value} | {status} |"
            )

        lines.extend([
            "",
            "---",
            "",
            "## Benchmark Results",
            "",
        ])

        for bench in self.benchmarks:
            lines.extend([
                f"### {bench['name']}",
                "",
                f"- Iterations: {bench['iterations']}",
                f"- Min: {bench['min_ms']:.2f} ms",
                f"- Max: {bench['max_ms']:.2f} ms",
                f"- Mean: {bench['mean_ms']:.2f} ms",
                f"- p50: {bench['p50_ms']:.2f} ms",
                f"- p95: {bench['p95_ms']:.2f} ms",
                f"- p99: {bench['p99_ms']:.2f} ms",
            ])
            if bench.get("memory_peak_mb"):
                lines.append(f"- Memory Peak: {bench['memory_peak_mb']:.2f} MB")
            if bench.get("error"):
                lines.append(f"- **Error**: {bench['error']}")
            lines.append("")

        if self.scaling_data:
            lines.extend([
                "---",
                "",
                "## Scaling Analysis",
                "",
            ])

            for metric_name, data_points in self.scaling_data.items():
                lines.extend([
                    f"### {metric_name}",
                    "",
                    "| Size | Mean (ms) | p95 (ms) | Memory (MB) |",
                    "|------|-----------|----------|-------------|",
                ])
                for point in data_points:
                    size = point.get("table_count") or point.get("row_count") or point.get("size")
                    mean = point.get("mean_ms", 0)
                    p95 = point.get("p95_ms", 0)
                    mem = point.get("memory_mb")
                    mem_str = f"{mem:.2f}" if mem else "N/A"
                    lines.append(f"| {size} | {mean:.2f} | {p95:.2f} | {mem_str} |")
                lines.append("")

        if self.notes:
            lines.extend([
                "---",
                "",
                "## Notes",
                "",
            ])
            for note in self.notes:
                lines.append(f"- {note}")

        lines.extend([
            "",
            "---",
            "",
            "*Report generated by DBMCP Performance Suite*",
        ])

        return "\n".join(lines)


class PerformanceReportGenerator:
    """Generates performance baseline reports."""

    def __init__(self):
        """Initialize report generator."""
        self.benchmark = Benchmark(track_memory=True)
        self.report = PerformanceReport(
            timestamp=datetime.now(),
            python_version=sys.version,
        )

    def run_all_benchmarks(self):
        """Run all performance benchmarks."""
        self._run_metadata_benchmarks()
        self._run_sample_data_benchmarks()
        # self._run_inference_benchmarks()  # Disabled: inference feature removed (007-analysis-tools)
        self._validate_nfrs()

    def _run_metadata_benchmarks(self):
        """Run metadata service benchmarks."""
        # Create test databases of varying sizes
        sizes = [25, 50, 100]
        scaling_data = []

        for size in sizes:
            engine = create_engine("sqlite:///:memory:", echo=False)
            with engine.connect() as conn:
                for i in range(size):
                    conn.execute(text(f"""
                        CREATE TABLE test_table_{i:04d} (
                            id INTEGER PRIMARY KEY,
                            name TEXT,
                            value REAL
                        )
                    """))
                conn.commit()

            service = MetadataService(engine)

            result = self.benchmark.run(
                name=f"list_tables_{size}",
                func=lambda: service.list_tables(limit=1000),
                iterations=3,
            )

            scaling_data.append({
                "table_count": size,
                "mean_ms": result.mean_ms,
                "p95_ms": result.p95_ms,
                "memory_mb": result.memory_peak_mb,
            })

            self.report.benchmarks.append(result.to_dict())
            engine.dispose()

        self.report.scaling_data["Metadata Retrieval"] = scaling_data

    def _run_sample_data_benchmarks(self):
        """Run sample data benchmarks."""
        sizes = [100, 500, 1000]
        scaling_data = []

        for size in sizes:
            engine = create_engine("sqlite:///:memory:", echo=False)
            with engine.connect() as conn:
                conn.execute(text("""
                    CREATE TABLE sample_table (
                        id INTEGER PRIMARY KEY,
                        name TEXT,
                        value REAL
                    )
                """))
                # Insert in batches
                for batch in range(0, size, 100):
                    end = min(batch + 100, size)
                    values = ", ".join(
                        f"('item_{i}', {i * 0.5})"
                        for i in range(batch, end)
                    )
                    if values:
                        conn.execute(text(f"""
                            INSERT INTO sample_table (name, value)
                            VALUES {values}
                        """))
                conn.commit()

            service = QueryService(engine)

            result = self.benchmark.run(
                name=f"sample_data_{size}_rows",
                func=lambda: service.get_sample_data(
                    table_name="sample_table",
                    schema_name="main",
                    sample_size=50,
                    sampling_method=SamplingMethod.TOP,
                ),
                iterations=3,
            )

            scaling_data.append({
                "row_count": size,
                "mean_ms": result.mean_ms,
                "p95_ms": result.p95_ms,
                "memory_mb": result.memory_peak_mb,
            })

            self.report.benchmarks.append(result.to_dict())
            engine.dispose()

        self.report.scaling_data["Sample Data Retrieval"] = scaling_data

